    private void txtCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCreateAccountActionPerformed
        // TODO add your handling code here:
        String routingNumber = txtRoutingNumber.getText();
        String accountNumber = txtAccountNumber.getText();
        String bankName = txtBankName.getText();
        String balanceTemp=String.valueOf(txtBalance.getText());
        int balance;
               
        if(routingNumber.isEmpty()||routingNumber.startsWith(" ")||accountNumber.isEmpty()
           ||accountNumber.startsWith(" ")||bankName.isEmpty()||bankName.startsWith(" ")
           ||balanceTemp.isEmpty()||balanceTemp.startsWith(" ")){
            JOptionPane.showMessageDialog(this,"All fields are mandatory and without spaces!!");
            return;
        }
        
        /*String bankNamePattern = "[a-zA-Z ]+";
        Pattern bankNamePattern = Pattern.compile(bankNamePattern);
        Matcher matchFName = bankNamePattern.matcher(txtBankName.getText());
        if (!matchFName.matches()) {
            JOptionPane.showMessageDialog(this, "Please enter only alphabets in first name!!");
            return;
        } else {
        }*/

        try{
            balance = Integer.parseInt(txtBalance.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Please enter balance in proper format!!");
            return;
        }
        
        Account account = ad.addAccount();
        account.setRoutingNumber(routingNumber);
        account.setAccountNumber(accountNumber);
        account.setBankName(bankName);
        account.setBalance(balance);
        
        JOptionPane.showMessageDialog(this, "Account successfully created!!");
        resetFields();
    }//GEN-LAST:event_txtCreateAccountActionPerformed

    private void resetFields(){
        txtRoutingNumber.setText("");
        txtAccountNumber.setText("");
        txtBankName.setText("");
        txtBalance.setText("");
    }
