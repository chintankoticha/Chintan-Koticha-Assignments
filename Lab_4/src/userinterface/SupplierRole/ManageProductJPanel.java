    public void refreshTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblProductList.getModel();
        dtm.setRowCount(0);
        for (Product p : supplier.getProductCatalog().getProductCatalog()) {
            Object[] row = new Object[3];
            row[0] = p;
            row[1] = p.getModelNumber();
            row[2] = p.getPrice();
            
            dtm.addRow(row);
        }
    }

