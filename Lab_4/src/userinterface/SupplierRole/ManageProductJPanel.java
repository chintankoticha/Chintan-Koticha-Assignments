    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProductList.getSelectedRow();
        if(selectedRow>=0)
        {
            Product p = (Product)tblProductList.getValueAt(selectedRow, 0);
            ViewProductDetailsJPanel panel = new ViewProductDetailsJPanel(userProcessContainer,p);
            userProcessContainer.add("ViewProductDetailsJPanel",panel);
            CardLayout layout= (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else{
            JOptionPane.showMessageDialog(this, "Please select a row from the table first!!","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewActionPerformed

