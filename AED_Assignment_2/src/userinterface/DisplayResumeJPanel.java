    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblResumeList.getModel();
        dtm.setRowCount(0);

        for (Resume resume : rh.getResumeHistory()) {
            Object row[] = new Object[3];
            row[0] = resume;
            row[1] = resume.getLastName();
            row[2] = resume.getContactNumber();
            dtm.addRow(row);
        }
    }

